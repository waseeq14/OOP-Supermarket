       private void displayCategories(){
            String[] columnNames = {"Id", "Name", "Description"};
            String [][] data = new String[cat.size()][];
            for(int i=0; i<cat.size();i++){
                 Category c = cat.get(i);
                 data[i] = new String[]{String.valueOf(c.getCatId()),c.getName(),c.getDesc()};
            }
            CategoryTable.setModel(new DefaultTableModel(data,columnNames));
        }

