    private void edit_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_edit_btnMouseClicked
        if(catId_field.getText().isEmpty()||catName_field.getText().isEmpty()||catDesc_field.getText().isEmpty())
        JOptionPane.showMessageDialog(this, "Missing Information!");
        else{
            try{
                Connection Con = Database.getInstance().getConnection();
                Statement St = Con.createStatement();
                String Query ="Update CATEGORYTBL set CATNAME='" + catName_field.getText()+"'"+",CATDESC='"+catDesc_field.getText()+"'"+"where CATID="+catId_field.getText();
                St.executeUpdate(Query);
                cat = Category.fetchCat();
                displayCategories();
                JOptionPane.showMessageDialog(this, "Category Updated!");
            }   catch (HeadlessException e) {
                e.printStackTrace();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Wrong Input");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Wrong Input");
            }catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_edit_btnMouseClicked

