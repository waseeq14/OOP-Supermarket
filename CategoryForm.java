    private void add_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_add_btnMouseClicked
        if (catId_field.getText().isEmpty() || catName_field.getText().isEmpty() || catDesc_field.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Missing Information!");
        } else {
            try {
                Connection Con = Database.getInstance().getConnection();
                Statement St = Con.createStatement();
                String query = "INSERT INTO CATEGORYTBL " + "VALUES( " + Integer.valueOf(catId_field.getText()) + ", '" + catName_field.getText() + "' , '"
                        + catDesc_field.getText() + "')";
                St.executeUpdate(query);
                cat = Category.fetchCat();
                displayCategories();
            } catch (HeadlessException e) {
                e.printStackTrace();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Wrong Input");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Wrong Input");
            }catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_add_btnMouseClicked

