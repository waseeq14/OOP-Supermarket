    private void del_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_del_btnMouseClicked
        if(catId_field.getText().isEmpty())
        JOptionPane.showMessageDialog(this, "Enter The Category To Be Deleted");
        else{
            try{
                Connection Con = Database.getInstance().getConnection();
                String cId = catId_field.getText();
                String Query = "Delete from CATEGORYTBL where CATID="+cId;
                Statement St = Con.createStatement();
                St.executeUpdate(Query);
                cat = Category.fetchCat();
                displayCategories();
                JOptionPane.showMessageDialog(this, "Category Deleted Successfully!");
            }catch (HeadlessException e) {
                e.printStackTrace();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Wrong Input");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Wrong Input");
            }catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_del_btnMouseClicked

