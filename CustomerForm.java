    public void displayCust(){
        String [] columnNames = {"ID","Name","Gender","Number","Address","Valid Till","Status"};
        String data[][] = new String[cus.size()][];
        for(int i=0;i<cus.size();i++){
            Customer c = cus.get(i);
            data[i] = new String []{String.valueOf(c.getId()),c.getName(),c.getGender(),c.getPhoneNumber(),c.getAddress(),c.getValidTill().toString(),c.getMemberStatus()};
        }
        CusTable.setModel(new DefaultTableModel(data,columnNames));
    }
