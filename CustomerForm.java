    private void delete_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_delete_btnMouseClicked
        if (cusId_field.getText().isEmpty())
            JOptionPane.showMessageDialog(this, "Enter The Product ID To Be Deleted");
        else {
            try {
                Connection Con = Database.getInstance().getConnection();
                String query = "Delete from CUSTOMERSTBL where CUSID = " + cusId_field.getText();
                Statement St = Con.createStatement();
                St.executeUpdate(query);
                cus = Customer.fetchCustomer();
                displayCust();
                JOptionPane.showMessageDialog(this, "Product Deleted Successfully!");
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_delete_btnMouseClicked

