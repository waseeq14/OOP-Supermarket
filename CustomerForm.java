    private void edit_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_edit_btnMouseClicked
        Date date = Date.valueOf(cusValid_field.getText());
        if (cusId_field.getText().isEmpty() || cusName_field.getText().isEmpty() || cusPhone_field.getText().isEmpty() || cusAddress_field.getText().isEmpty()
                || cusValid_field.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Missing Information!");
        } else if (Customer.datePassed(date)) {
            JOptionPane.showMessageDialog(this, "Invalid Date!");
            cusValid_field.setText("");
        } else {
            try {
                Connection Con = Database.getInstance().getConnection();
                PreparedStatement add = Con.prepareStatement("UPDATE CUSTOMERSTBL SET CUSNAME=?, CUSGENDER=?, CUSNUMBER=?, CUSADDRESS=?, VALIDTILL=?, "
                        + "CUSSTATUS=? WHERE CUSID=?");
                add.setString(1, cusName_field.getText());
                add.setString(2, GenderCb.getSelectedItem().toString());
                add.setString(3, cusPhone_field.getText());
                add.setString(4, cusAddress_field.getText());
                add.setDate(5, date);
                add.setString(6, CusStatusCb.getSelectedItem().toString());
                add.setInt(7, Integer.parseInt(cusId_field.getText()));
                add.executeUpdate();
                cus = Customer.fetchCustomer();
                displayCust();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_edit_btnMouseClicked

