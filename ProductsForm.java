    private void ProductTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ProductTableMouseClicked
        DefaultTableModel model = (DefaultTableModel)ProductTable.getModel();
        int Myindex = ProductTable.getSelectedRow();
        prodId_field.setText(model.getValueAt(Myindex, 0).toString());
        prodName_field.setText(model.getValueAt(Myindex, 1).toString());
        prodQty_field.setText(model.getValueAt(Myindex, 2).toString());
        prodPrice_field.setText(model.getValueAt(Myindex, 3).toString());
    }//GEN-LAST:event_ProductTableMouseClicked

    private void del_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_del_btnMouseClicked
        if(prodId_field.getText().isEmpty())
            JOptionPane.showMessageDialog(this, "Enter The Product ID To Be Deleted");
        else{
            try{
               Connection Con = Database.getInstance().getConnection();
               String Query = "Delete from PRODUCTTBL where PRODID="+prodId_field.getText();
               Statement Add = Con.createStatement();
               Add.executeUpdate(Query);
               products = Product.fetchProducts();
               displayProducts();
               JOptionPane.showMessageDialog(this, "Product Deleted Successfully!");
            }
            catch(SQLSyntaxErrorException e){
             JOptionPane.showMessageDialog(this, "Wrong Input");
        }
            catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Wrong Input");
        }catch(Exception e){
                e.printStackTrace();
            }
        }     
    }//GEN-LAST:event_del_btnMouseClicked

