    private void displayProducts() {
        String[] columnNames = {"ID", "Name", "Qty","Price", "Category"};
        String[][] data = new String[products.size()][];
        for (int i = 0; i < products.size(); i++) {
            Product product = products.get(i);
            data[i] = new String[] {
                String.valueOf(product.getId()),
                product.getName(), String.valueOf(product.getQuantity()),
                String.valueOf(product.getPrice()), product.getCategory()
            };
        }
        
        ProductTable.setModel(new DefaultTableModel(data, columnNames));
    }
    private void GetCat(){
        ArrayList<Category> cat = Category.fetchCat();
        for (Category cat1: cat) {
            catCb.addItem(cat1.getName());
        }
    }

