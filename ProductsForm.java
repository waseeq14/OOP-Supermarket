    private void add_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_add_btnMouseClicked
        if(prodId_field.getText().isEmpty()||prodName_field.getText().isEmpty()||prodPrice_field.getText().isEmpty()||prodQty_field.getText().isEmpty())
        {
         JOptionPane.showMessageDialog(this, "Missing Information!");   
        }
        else{
        try{
            Connection Con = Database.getInstance().getConnection();
            Statement St = Con.createStatement();
            PreparedStatement add = Con.prepareStatement("insert into PRODUCTTBL values(?,?,?,?,?)");
            add.setInt(1, Integer.parseInt(prodId_field.getText()));
            add.setString(2, prodName_field.getText());
            add.setString(3, prodQty_field.getText());
            add.setString(4, prodPrice_field.getText());
            add.setString(5, catCb.getSelectedItem().toString());
            add.executeUpdate();
            products = Product.fetchProducts();
            displayProducts();
        } catch(DerbySQLIntegrityConstraintViolationException e){
            JOptionPane.showMessageDialog(this, "ID ALREADY EXISTS!");
            prodId_field.setText("");
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Wrong Input");
        }
        catch (Exception e) {
            e.printStackTrace();
        }}
    }//GEN-LAST:event_add_btnMouseClicked

